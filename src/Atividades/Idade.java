//Crie um programa que converta a idade de uma pessoa em dias, horas, minutos e segundos.
package Atividades;

public class Idade {
    public static void main(String[] args) {
        int idade = 20;
        System.out.println("Idade em dias: "+idade*365+", em horas "+idade*8760+", em minutos "+idade*3.154e+7+", em segundos "+idade*525600);
    }
}
